#include "libft.h"

char		*ft_strcpy(char *dest, const char *src)
{
	size_t m;
	size_t n;

	m = 0;
	n = 0;
	while (src[m] != '\0')
		dest[n++] = src[m++];
	dest[n] = '\0';
	return (dest);
}
