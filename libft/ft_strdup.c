#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dup;
	int		i;

	i = 0;
	while (src[i] != '\0')
		i++;
	if ((char*)malloc(sizeof(*dup) * (i + 1)) == NULL)
		return (NULL);
	else
		dup = (char*)malloc(sizeof(*dup) * (i + 1));
	i = 0;
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
